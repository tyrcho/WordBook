package info.daviot.dictionary.model.factory;

public class DictionnaryFactoryException extends Exception {

    public DictionnaryFactoryException() {
        super();
    }

    public DictionnaryFactoryException(String message) {
        super(message);
    }

    public DictionnaryFactoryException(String message, Throwable cause) {
        super(message, cause);
    }

    public DictionnaryFactoryException(Throwable cause) {
        super(cause);
    }

}
